export function readMembers(): AssociationMember[] {
  const stored = localStorage.getItem(membersStorageKey);
  if (!stored) return officialBureauMembers;
  try {
    const parsed = JSON.parse(stored) as AssociationMember[];
    const byId = new Map(parsed.map((member) => [member.id, member]));
    officialBureauMembers.forEach((member) => {
      if (!byId.has(member.id)) byId.set(member.id, member);
    });
    return [...byId.values()];
  } catch {
    return officialBureauMembers;
  }
}

export function saveMembers(members: AssociationMember[]) {
  localStorage.setItem(membersStorageKey, JSON.stringify(members));
}
